EMAIL_RE = re.compile(r"^[^\s@]+@[^\s@]+\.[^\s@]+$")
SUBMISSIONS = []


def requests_page():
    rows = "".join(
        "<tr>"
        f"<td>{escape(s['created'])}</td><td>{escape(s['name'])}</td>"
        f"<td>{escape(s['email'])}</td><td>{escape(s['phone'])}</td>"
        f"<td>{escape(s['room'])}</td><td>{escape(s['pattern'])}</td>"
        f"<td>{escape(s['details'][:120])}</td></tr>"
        for s in reversed(SUBMISSIONS)
    ) or "<tr><td colspan='7' style='padding:24px;color:#888'>No requests yet.</td></tr>"
    return (
        "<!doctype html><meta charset='utf-8'><title>Fitting requests</title>"
        "<style>body{font-family:Georgia,serif;background:#F7F1E7;color:#221C17;margin:40px}"
        "table{border-collapse:collapse;width:100%;background:#fff}"
        "th,td{border:1px solid #e2dac9;padding:8px 10px;text-align:left;font-size:14px}"
        "th{background:#9E1B22;color:#F7F1E7}</style>"
        f"<h1>Fitting requests ({len(SUBMISSIONS)})</h1>"
        "<table><tr><th>Received (UTC)</th><th>Name</th><th>Email</th><th>Phone</th>"
        "<th>Space</th><th>Pattern</th><th>Notes</th></tr>"
        f"{rows}</table>"
    )


class Handler(BaseHTTPRequestHandler):
    def _send(self, code, body, ctype="text/html; charset=utf-8"):
        data = body.encode("utf-8") if isinstance(body, str) else body
        self.send_response(code)
        self.send_header("Content-Type", ctype)
        self.send_header("Content-Length", str(len(data)))
        self.end_headers()
        self.wfile.write(data)

    def do_GET(self):
        if self.path == "/":
            self._send(200, PAGE)
        elif self.path == "/health":
            self._send(200, json.dumps({"ok": True}), "application/json")
        elif self.path == "/requests":
            self._send(200, requests_page())
        else:
            self._send(404, "Not found")

    def do_POST(self):
        if self.path != "/book":
            return self._send(404, "Not found")
        length = int(self.headers.get("Content-Length", 0) or 0)
        try:
            data = json.loads(self.rfile.read(length) or b"{}")
        except Exception:
            data = {}
        name = (data.get("name") or "").strip()
        email = (data.get("email") or "").strip()
        phone = (data.get("phone") or "").strip()
        room = (data.get("room") or "").strip()
        err = None
        if not name:
            err = "Please enter your name."
        elif not EMAIL_RE.match(email):
            err = "Please enter a valid email."
        elif not phone:
            err = "Please enter a phone number."
        elif not room:
            err = "Please choose a space."
        if err:
            return self._send(400, json.dumps({"ok": False, "error": err}), "application/json")
        SUBMISSIONS.append({
            "created": datetime.now(timezone.utc).isoformat(timespec="seconds"),
            "name": name, "email": email, "phone": phone, "room": room,
            "pattern": (data.get("pattern") or "").strip(),
            "details": (data.get("details") or "").strip(),
        })
        first = name.split(" ")[0]
        self._send(200, json.dumps({
            "ok": True,
            "message": f"How lovely, {first} - your request is on its way. "
                       "Beau will be in touch within a day.",
        }), "application/json")

    def log_message(self, *args):
        pass


